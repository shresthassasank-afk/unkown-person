public class DataTypeInspector
{
    public static void main(String[] args){
        byte b=30;
        short s=400;
        int i=200_000;
        long l=30_000_00L;
        float f=20.0F;
        double d=300.00D;
        char c='q';
        boolean t =true;
        System.out.println("The value of byte:" +b); //Size: 8-bit 
        System.out.println("The value of short:" +s);//Size: 16-bit
        System.out.println("The value of int:" +i);//Size: 32-bit 
        System.out.println("The value of long:" +l);//Size: 64-bit
        System.out.println("The value of float:"+f);//Size: 32-bit
        System.out.println("The value of double:" +d);//Size: 64-bit
        System.out.println("The value of char:" +c);//Size: 16-bit Unicode character.
        System.out.println("The value of boolean:" +t);//size:1-bit
        
        
        

        
        
        
        
    }
    
        
        
        
        
    
}